package com.tyss.capgemini.methods;

public class AreaCalculator {
	public  int calcSquareArea(int side) {
		return side*side;
	}
	public  int calcAreaOfRectangle(int length, int width) {
		return length*width;
		
	}
	public  double calcAreaOfcircle(int radius)
	{
		return 3.14*radius*radius;
	}
}
